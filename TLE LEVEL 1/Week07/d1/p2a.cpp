// it will also give tle
void solve(){
	int n;
	cin >> n;

	int ans = -1;

	for(int i=1; i<n; i++){
		for(int j=i+1; j<=n; j++){
			ans = max(ans, __gcd(i,j));
		}
	}

	cout << ans << endl;
}


